def luas_segitiga() :
    a = int(input("masukkan jumlah alas segitiga: "))
    t = int(input("masukkan jumlah tinggi segitiga: "))
    luas = a*t/2
    print("Luas segitiga adalah: ", luas)

luas_segitiga()

def luas_persegi_panjang() :
    l = int(input("masukkan jumlah lebar persegi panjang: "))
    p = int(input("masukkan jumlah panjang persegi panjang: "))
    luas = p*l
    print("Luas persegi panjang adalah: ", luas)

luas_persegi_panjang()

def luas_persegi() :
    s = int(input("masukkan jumlah sisi persegi: "))
    luas = s*s
    print("Luas persegi adalah: ", luas)

luas_persegi()